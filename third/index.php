echo "<p><b>Пример 3:</b> Векторы разной размерности [1, 2, 3] и [1, 2]<br>";
echo "Результат: " . ($result !== null ? $result : "Ошибка (как и ожидалось)") . "</p>";

// Пример 4: Вектор с нечисловым элементом (из исходного примера)
$vector7 = [2, 3, 'foo'];

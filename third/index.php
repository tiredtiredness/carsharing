$result = distance($vector7, $vector8);
echo "<p><b>Пример 4:</b> Вектор с нечисловым элементом [2, 3, 'foo'] и [5.3, 4.3, -1.6]<br>";
echo "Результат: " . ($result !== null ? $result : "Ошибка (как и ожидалось)") . "</p>";

// Пример 5: Одномерные векторы

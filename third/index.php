<?php
// Подключаем файл с функцией
include 'distance.php';

echo "<h2>Примеры вычисления расстояния между векторами</h2>";

// Пример 1: Корректные векторы
$vector1 = [1, 2, 3];
$vector2 = [4, 5, 6];
$result = distance($vector1, $vector2);
echo "<p><b>Пример 1:</b> Векторы [1, 2, 3] и [4, 5, 6]<br>";
echo "Результат: " . ($result !== null ? $result : "Ошибка") . "</p>";

// Пример 2: Векторы с плавающей точкой
$vector3 = [1.5, 2.3, 3.1];
